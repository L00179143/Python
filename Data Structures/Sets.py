# Set is a data type in python used to store several items in a single variable. 
# It is one of the four built-in data types (List, Dictionary, Tuple, and Set) 
# having qualities and usage different from the other three. 
# Sets are unordered, so you cannot be sure in which order the items will appear.
# True and 1 are same . False and 0 are same.

setcheck = {"alto", "baleno", "cat", "onda"}
print(setcheck)

setcheck = {"alto", "baleno", "cat", "onda", True, 1, 2}
print(setcheck)

setcheck = {"alto", "baleno", "cat", "onda", False, 1, 0}
print(setcheck)
