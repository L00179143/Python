# Assignment and Variables Example
# Think of a variable as a name attached to a particular object. 
# In Python, variables need not be declared or defined in advance, as is the case in many other programming languages. To create a variable, you just assign it a value and then start using it. 
# Assignment is done with a single equals sign (=):


# Below we are assigning values to variables named length and width.
length = 10
width = 2

# These variables are used to calculate area
area = length * width

# Print the values and the calculated area
print("Length is:", length)
print("Width is:", width)
print("Area of the provided Length and Width is", area)

