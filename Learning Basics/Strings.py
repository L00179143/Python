# Python does not have "character" data type, a single character is simply a "string" with a length of 1.
# So, Strings in python are created using single quote or double quote or triple quotes. 

# String with single Quotes 
Type1 = 'I just have one quote'
print("This string was defined with Single Quotes: ") 
print(Type1) 
  
# String with double Quotes 
Type2 = "I actually have 2 quotes"
print("\nThis String was displayed with Double Quotes: ") 
print(Type2)


  