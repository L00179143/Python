# There are several basic datatypes built into python. Numeric, String and Boolean.
# Numeric data tu[e represents data that has numeric value. Numberic value can be integer, floating number or even a complex number. 
# Below program will help to determine the numberic and string data type. 

# Integer numeric type
a = 100
